    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     try {
            Connection conn = config.Koneksi.getKoneksi();
            String sql = "INSERT INTO produk (nama_produk, harga_beli, harga_jual, stok, satuan) VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtNama.getText());
            pst.setString(2, txtBeli.getText());
            pst.setString(3, txtJual.getText());
            pst.setString(4, txtStok.getText());
            pst.setString(5, cbSatuan.getSelectedItem().toString());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data ATK Berhasil Disimpan!");
            tampilData(); 
            bersihForm(); 
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = config.Koneksi.getKoneksi();
            String sql = "UPDATE produk SET nama_produk=?, harga_beli=?, harga_jual=?, stok=?, satuan=? WHERE id_produk=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtNama.getText());
            pst.setString(2, txtBeli.getText());
            pst.setString(3, txtJual.getText());
            pst.setString(4, txtStok.getText());
            pst.setString(5, cbSatuan.getSelectedItem().toString());
            pst.setString(6, txtId.getText()); 

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data ATK Berhasil Diupdate!");
            tampilData();
            bersihForm();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Edit: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int opsi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if (opsi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = config.Koneksi.getKoneksi();
                String sql = "DELETE FROM produk WHERE id_produk=?";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, txtId.getText());

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                tampilData();
                bersihForm();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

