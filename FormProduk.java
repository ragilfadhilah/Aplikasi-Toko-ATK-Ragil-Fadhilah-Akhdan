public void inisialisasiTabel() {
        model = new DefaultTableModel();
        tblProduk.setModel(model);
        model.addColumn("ID");
        model.addColumn("Nama ATK");
        model.addColumn("Harga Beli");
        model.addColumn("Harga Jual");
        model.addColumn("Stok");
        model.addColumn("Satuan");
    }

    private void tampilData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection conn = config.Koneksi.getKoneksi();
            Statement stmt = conn.createStatement();
            String sql = "SELECT * FROM produk";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                Object[] obj = new Object[6];
                obj[0] = rs.getString("id_produk");
                obj[1] = rs.getString("nama_produk");
                obj[2] = rs.getString("harga_beli");
                obj[3] = rs.getString("harga_jual");
                obj[4] = rs.getString("stok");
                obj[5] = rs.getString("satuan");
                model.addRow(obj);
            }
        } catch (SQLException e) {
            System.out.println("Gagal memuat data: " + e.getMessage());
        }
    }

